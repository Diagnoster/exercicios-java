package exercicios;

public class ExercicioCelsiusParaFahrenheit {
	
	public static void main(String[] args) {
		// CALCULANDO FAHRENHEIT , USANDO A LÓGICA DE CELSIUS * 1.8 + 32 DE AJUSTE.
		
		double celsius = 20;
		final double ajuste = 32;
		double fahrenheit = celsius * 1.8 + ajuste;
		
		System.out.println(fahrenheit);
		
		
	}

}
